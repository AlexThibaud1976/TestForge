export function buildUserPrompt(
  title: string, description: string, acceptanceCriteria: string | null,
  useImprovedVersion: boolean, improvedVersion: string | null,
): string {
  const us = useImprovedVersion && improvedVersion
    ? improvedVersion
    : `**Titre :** ${title}\n\n**Description :**\n${description || '(aucune)'}\n\n**Critères d\'acceptance :**\n${acceptanceCriteria || '(aucun)'}`;
  return `Génère les tests Playwright JavaScript (ESM) pour :\n\n${us}\n\nGénère exactement 3 fichiers (page_object .js, test_spec .js, fixtures .json). JavaScript valide avec ES modules.`;
}
